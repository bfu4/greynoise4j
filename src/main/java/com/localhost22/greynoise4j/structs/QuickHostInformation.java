package com.localhost22.greynoise4j.structs;

public class QuickHostInformation {

    String ip;
    boolean noise;
    boolean riot;
    String code;

    public QuickHostInformation() {

    }

    public String getIp() {
        return ip;
    }

    public String getCode() {
        return code;
    }

    public boolean isRiot() {
        return riot;
    }

    public boolean isNoise() {
        return noise;
    }
}
